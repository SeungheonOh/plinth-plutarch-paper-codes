{-# OPTIONS_GHC -fplugin Plinth.Plugin #-}

module E12Plinth where

import PlutusTx.AsData (asData)
import PlutusTx.Prelude

asData
  [d|
    data Withdraw
      = Amount Integer
      | Joint Withdraw Withdraw
      | Deduct Integer Withdraw
      deriving newtype (UnsafeFromData, ToData)
    |]

-- BUG: netWithdraw Deduct n from is parsed as four arguments to netWithdraw
-- instead of netWithdraw (Deduct n from)
netWithdraw :: Withdraw -> Integer
netWithdraw w = case w of
  Amount n -> n
  Joint x y -> netWithdraw x + netWithdraw y
  Deduct n from -> netWithdraw Deduct n from
